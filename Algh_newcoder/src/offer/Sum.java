package offer;
	/*求1+2+3+...+n，要求不能使用乘除法、for、while、if、else、switch、case等关键字
	及条件判断语句（A?B:C）*/
public class Sum {
	public static int Sum_Solution(int n) {    
		return (n > 0) ? n + Sum_Solution(n - 1) : 0;
    }
	public static void main(String[] args) {
		System.out.println(Sum_Solution(3));
	}
}
